char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	i2;

	if (*to_find == 0)
		return (str);
	i = 0;
	while (str[i] != '\0')
	{
		i2 = 0;
		while (to_find[i2] == str[i + i2])
		{
			if (to_find[i2 + 1] == '\0')
			{
				return (str + i);
			}
			i2++;
		}
		i++;
	}
	return (0);
}
