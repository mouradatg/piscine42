#include <unistd.h>

int	ft_atoi(char *str)
{
	int	nbr;
	int	neg;

	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	neg = 0;
	while (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
			neg = 1 - neg;
	}
	nbr = 0;
	while (*str >= '0' && *str <= '9')
		nbr = nbr * 10 + *str++ - '0';
	if (neg)
		nbr *= -1;
	return (nbr);
}
